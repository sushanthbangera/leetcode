package com.leetcode.utils;

public class ArrayUtils {

	public static void print(String[] arr) {
		if (arr != null) {
			for (String s : arr) {
				System.out.println(s);
			}
		}
	}

	public static void print(Integer[] arr) {
		if (arr != null) {
			for (Integer s : arr) {
				System.out.println(s);
			}
		}
	}
}
